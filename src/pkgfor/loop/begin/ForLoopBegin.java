
package pkgfor.loop.begin;


public class ForLoopBegin {

    public static void main(String[] args) {
        {
            System.out.println("If number want to increase");
        }
        for(int x=0;x<10;x++){
            System.out.println(x);
        }
        {
            System.out.println("Next,if number wants to discrease");
        }
        for(int y=6;y>2;y--){
            System.out.println(y);
        }
        {
            System.out.println("When count must stop at given number");
        }
        for(int z=0;z<=5;z++){
            System.out.println(z);
        }
    }
    
}
